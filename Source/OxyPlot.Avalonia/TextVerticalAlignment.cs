using System;
using System.Collections.Generic;
using System.Text;

namespace OxyPlot.Avalonia
{
    /// <summary>
    /// Specifies vertical alignment for text.
    /// </summary>
    public enum TextVerticalAlignment
    {
        /// <summary>
        /// Aligned at the top.
        /// </summary>
        Top = -1,

        /// <summary>
        /// Aligned in the middle.
        /// </summary>
        Middle = 0,

        /// <summary>
        /// Aligned at the bottom.
        /// </summary>
        Bottom = 1,

        /// <summary>
        /// Aligned at the baseline.
        /// </summary>
        Baseline = 2,
    }
}
